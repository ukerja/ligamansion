        $(document).ready(function () {

            var length = $("div.promotionpopupslide").length;

            $("div.promotionpopupslide").each(function () {

                $(this).hide();

            });

            $("a.clickformore_btn").click(function () {

                var index = parseInt($(this).attr("class").replace(/[^0-9]/g, ""));

                for (var i = 1; i <= length; i++) {

                    if (i == index) {

                        $(".hideme_" + i).show();

                    } else {

                        $(".hideme_" + i).hide();

                    }

                }

            });

            $(".close").click(function () {

                $("div.promotionpopupslide").each(function () {

                    $(this).hide();

                });

            });


            //target to  Promotion and open details of promotion
            var pid = getUrlVars()["pid"];
            if (pid != null || pid != "") {

                //$([document.documentElement, document.body]).animate({
                //    scrollTop: $("#target" + pid).offset().top
                //}, 2000);

                targetPromotion(pid);
            }

            function targetPromotion(targetID) {
                $(".hideme_" + targetID).show();
            }

            function getUrlVars() {
                var vars = [],
                    hash;
                var hashes = window.location.href.slice(window.location.href.indexOf('?') + 1).split('&');
                for (var i = 0; i < hashes.length; i++) {
                    hash = hashes[i].split('=');
                    vars.push(hash[0]);
                    vars[hash[0]] = hash[1];
                }
                return vars;
            }

        });


